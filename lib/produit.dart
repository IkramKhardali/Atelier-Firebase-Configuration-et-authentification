void addNewProduct() {
  Produit newProduct = Produit(
    id: '', // Suppose id is auto-generated by Firestore
    marque: "Nike",
    designation: "Air Max",
    categorie: 'Classic running',
    prix: 94.99, // Specify the type (double)
    photo:
        "https://static.nike.com/a/images/t_PDP_1728_v1/f_auto,q_auto:eco/wzitsrb4oucx9jukxsmc/chaussure-air-max-90-pour-dlXJdc.png",
    quantite: 155, // Specify the type (int)
  );

  FirebaseFirestore.instance.collection('produits').add(newProduct.toJson());
}

void deleteProduct(String productId) {
  FirebaseFirestore.instance.collection('produits').doc(productId).delete();
}
